    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
        String user=jTextField1.getText().trim();
        int uno=Integer.parseInt(user);
           if(user.equals(""))
               //jButton1.setEnabled(false);
               JOptionPane.showMessageDialog(null, "Enter the number");
//            result.setText("first enter a number");
       
       else
           {
               if(count==0)
                   count=0;
               else
               {
               count--;
                cstr=Integer.toString(count);
                Clable.setText(cstr);
                    jl[count1].setText(user);
                    count1++;
               }
                
             if(uno==random)
             {
            result.setText("You win! Correct Number");
            jTextField1.setEditable(false);
            jButton1.setEnabled(false);
             }
                           
             else if(count<=0)
             {
            result.setText("Game Over! Number is "+str+" ");
            jTextField1.setText("");
            jButton1.setEnabled(false);
             }
        
            else if(uno<=0 ||  uno>100)
            {
            result.setText("Your Number is Invalid ");
            jTextField1.setText("");
            }
        
            else if(uno<random)
            {
            result.setText("Your Guess is  Low!");
            jTextField1.setText("");
            }
        
            else if(uno>random)
            {
            result.setText("Your Guess is  High!");
            jTextField1.setText("");
           }
         

           
        }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

